package bean;

public class student {
private Integer id;
private String name;
private cards card;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public cards getCard() {
        return card;
    }

    public void setCard(cards card) {
        this.card = card;
    }
}
